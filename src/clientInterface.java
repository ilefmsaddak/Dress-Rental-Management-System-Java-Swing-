public void validateFields() {
    String nomText = nom.getText().trim();
    String prenomText = prenom.getText().trim();
    String adrText = adresse.getText().trim();
    String emailText = email.getText().trim();
    String telText = tel.getText().trim();


    if(
        !nomText.isEmpty() &&
        !prenomText.isEmpty() &&
        !adrText.isEmpty() &&
        !emailText.isEmpty() &&
        !telText.isEmpty())
       {
        update.setEnabled(true);}
    else{        update.setEnabled(false);}

    }
    

public Client info() { 
    Client client = new Client();     

    client.id = Integer.parseInt(idn.getText());
    client.nom = nom.getText();
    client.prenom = prenom.getText();
    client.adresse = adresse.getText();
    client.email = email.getText();
    client.tel = tel.getText();

    return client;
}







